/**
 * 生成完整的提示词
 * @param {string} type - 转换类型 ('direct-to-polite' 或 'polite-to-direct')
 * @param {string} style - 风格类型 ('gentle', 'strict', 'neutral', 'emotional')
 * @param {string} relationship - 关系类型 ('leader', 'colleague', 'subordinate', 'client', 'supplier')
 * @param {string} inputText - 输入文本
 * @returns {string} 完整的提示词
 */
function generatePrompt(type, style, relationship, inputText) {
    const relationshipInfo = relationshipConfig[relationship] || relationshipConfig.leader;
    
    let template;
    if (type === 'direct-to-polite') {
        const stylePrompts = directToPolitePrompts[style] || directToPolitePrompts.gentle;
        template = stylePrompts[relationship] || stylePrompts.leader;
    } else {
        // polite-to-direct 现在只有关系类型，没有风格区分
        template = politeToDirectPrompts[relationship] || politeToDirectPrompts.leader;
    }
    
    return template.replace('{input}', inputText);
}

